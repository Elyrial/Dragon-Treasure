// Player klassen
public class Player {
    private String name;
    
// Konstruktör för angiven parameter
    public Player(String name) {
        this.name = name;
    }
    
// Setter & getter för att returnera namn
    public String getName() {
       return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
}
